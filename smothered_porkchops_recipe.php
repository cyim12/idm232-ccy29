            <div class="ingredients">
                <h2 class="recipe_headings">INGREDIENTS</h2>
                <ul class="ingred">
                    <li>8  thick, bone-in pork chops (more if using smaller chops)</li>
                    <br>
                    <li>Anise brine</li>
                    <br>
                    <li>4  tablespoons neutral oil, like peanut or safflower, or lard</li>
                    <br>
                    <li>1  cup all-purpose flour</li>
                    <br>
                    <li>8  medium yellow onions, peeled and thinly sliced</li>
                    <br>
                    <li>1  bay leaf</li>
                    <br>
                    <li>Kosher salt and freshly ground pepper to taste</li>
                    <br>
                    <li>2  quarts pork stock or chicken stock</li>
                    <br>
                    <li>1  tablespoon finely chopped parsley, optional</li>
                </ul>
            </div>
            <div class="directions">
                <h2 class="recipe_headings">DIRECTIONS</h2>
                <ul class="direct">
                    <li id="step">STEP 1:</li>
                    <li>In a large nonreactive container, submerge the pork in the brine and place in refrigerator for at least 12 hours or overnight, then remove the chops and dry well with paper towels.</li>
                    <br>
                    <li id="step">STEP 2:</li>
                    <li>Heat oven to 325. Heat the oil or lard in a large ovenproof pot or Dutch oven set over medium heat. Dredge the chops in the flour, shaking off the excess. Reserve the leftover flour.</li>
                    <br>
                    <li id="step">STEP 3:</li>
                    <li>Cook the chops in the fat, about 4 minutes per side, until they are brown and crisp on the exterior, and transfer to a plate. Reduce heat to medium-low. Add the onions and bay leaf and cook, stirring often, until the onions are softened, about 15 minutes. Season well with salt and pepper.</li>
                    <br>
                    <li id="step">STEP 4:</li>
                    <li>Add 6 tablespoons flour to the onions, stir well and allow to cook 3 or 4 minutes. Slowly add the stock, stirring and scraping the pan well, until it is incorporated and the mixture is slightly thickened.</li>
                    <br>
                    <li id="step">STEP 5:</li>
                    <li>Return chops to the pot and bring to a simmer, then cover tightly and cook in the oven until very tender, 2 or more hours. Transfer chops to a plate and place pot on stovetop, over medium heat. Reduce sauce until it is thick enough to resemble gravy, skimming excess oil and foam if necessary. Return chops to pot to reheat, then serve with their gravy over rice or mashed potatoes. Garnish with parsley if you like.</li>
                </ul>
